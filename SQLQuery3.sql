CREATE TABLE hasta (
    tc_no NVARCHAR(11) PRIMARY KEY,
    ad NVARCHAR(50),
    soyad NVARCHAR(50),
    sehir NVARCHAR(50),
    tel NVARCHAR(20)
);


CREATE TABLE eczane (
    eczane_id INT PRIMARY KEY IDENTITY(1,1),
    ad NVARCHAR(100),
    sehir NVARCHAR(50),
    telefon NVARCHAR(20),
    email NVARCHAR(100),
    semt NVARCHAR(50)
);


CREATE TABLE doktor (
    sicil_no INT PRIMARY KEY,
    ad NVARCHAR(50),
    soyad NVARCHAR(50),
    bolum NVARCHAR(100),
    maas DECIMAL(10,2)
);


CREATE TABLE recete (
    recete_id INT PRIMARY KEY IDENTITY(1,1),
    tarih DATE,
    ilac_ad NVARCHAR(100),
    ilac_fiyat DECIMAL(10,2),
    tutar DECIMAL(10,2),
    eczane_id INT,
    FOREIGN KEY (eczane_id) REFERENCES eczane(eczane_id)
);


CREATE TABLE hasta_detay (
    s_no INT PRIMARY KEY IDENTITY(1,1),
    dr_no INT,
    ecz_no INT,
    h_tcno NVARCHAR(11),
    mua_tarih DATE,
    recete_no INT,
    FOREIGN KEY (dr_no) REFERENCES doktor(sicil_no),
    FOREIGN KEY (ecz_no) REFERENCES eczane(eczane_id),
    FOREIGN KEY (h_tcno) REFERENCES hasta(tc_no),
    FOREIGN KEY (recete_no) REFERENCES recete(recete_id)
);


CREATE TABLE tahlil (
    s_no INT PRIMARY KEY IDENTITY(1,1),
    tc_no NVARCHAR(11),
    t_adi NVARCHAR(100),
    t_kodu NVARCHAR(50),
    tarih DATE,
    t_fiyat DECIMAL(10,2),
    FOREIGN KEY (tc_no) REFERENCES hasta(tc_no)
);


